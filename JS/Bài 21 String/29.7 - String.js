//  startWith - endsWith

// 1. startsWith - Kiểm tra xem chuỗi có bắt đầu bằng một chuỗi con hay không
let str = "Hello, World!";
console.log(str.startsWith("Hello")); // Kết quả: true
console.log(str.startsWith("World")); // Kết quả: false

// 2. endsWith - Kiểm tra xem chuỗi có kết thúc bằng một chuỗi con hay không
let str2 = "Hello, World!";
console.log(str2.endsWith("World!")); // Kết quả: true
console.log(str2.endsWith("Hello")); // Kết quả: false