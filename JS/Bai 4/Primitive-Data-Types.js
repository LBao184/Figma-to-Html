// 1. Number
let age = 25; // Số nguyên
let price = 19.99; // Số thập phân
console.log("Age:", age);
console.log("Price:", price);

// 2. String
let name = "John Doe"; // Chuỗi ký tự
let greeting = 'Hello, World!';
console.log("Name:", name);
console.log("Greeting:", greeting);

// 3. Boolean
let isStudent = true; // Giá trị đúng
let hasGraduated = false; // Giá trị sai
console.log("Is Student:", isStudent);
console.log("Has Graduated:", hasGraduated);

// 4. Undefined
let address; // Biến chưa được gán giá trị
console.log("Address:", address);

// 5. Null
let phoneNumber = null; // Biến được gán giá trị null
console.log("Phone Number:", phoneNumber);

// 6. Symbol
let uniqueId = Symbol("id");
console.log("Unique ID:", uniqueId);