//  replace - repeat

// 1. replace - Thay thế một chuỗi con bằng một chuỗi khác
let str = "Hello, World!";
console.log(str.replace("World", "JavaScript")); // Kết quả: "Hello, JavaScript!"

// 2. repeat - Lặp lại chuỗi một số lần
let str2 = "JavaScript ";
console.log(str2.repeat(3)); // Kết quả: "JavaScript JavaScript JavaScript "