// Chào mừng bạn đến với JavaScript 🎉

// 1. In ra màn hình
console.log("Xin chào Lê Thiên Bảo! 👋");

// 2. Biến và phép tính
let a = 10;
let b = 5;
let tong = a + b;
console.log("Tổng của a + b là:", tong);

// 3. Hàm (function)
function chao(name) {
  return "Xin chào, " + name + "!";
}
console.log(chao("Bảo"));

// 4. Cấu trúc điều kiện
if (tong > 10) {
  console.log("Kết quả lớn hơn 10 ✅");
} else {
  console.log("Kết quả nhỏ hơn hoặc bằng 10 ❌");
}

// 5. Vòng lặp (loop)
for (let i = 1; i <= 5; i++) {
  console.log("Vòng lặp thứ", i);
}

console.log("🎯 Kết thúc chương trình!");
