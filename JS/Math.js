// CÁC PHÉP TOÁN CƠ BẢN TRONG JAVASCRIPT
let a = 5;
let b = 3;

// Phép cộng
let sum = a + b;
console.log("Tổng của a và b là: " + sum);

// Phép trừ
let difference = a - b;
console.log("Hiệu của a và b là: " + difference);

// Phép nhân
let product = a * b;
console.log("Tích của a và b là: " + product);

// Phép chia
let quotient = a / b;
console.log("Thương của a và b là: " + quotient);

// Phép chia lấy dư
let remainder = a % b;
console.log("Phần dư của a chia b là: " + remainder);
